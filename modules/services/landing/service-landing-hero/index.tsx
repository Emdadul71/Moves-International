const ServiceLandingHero = () => {
  const shuffle = (array: any) => {
    let currentIndex = array.length,
      randomIndex;

    while (currentIndex != 0) {
      randomIndex = Math.floor(Math.random() * currentIndex);
      currentIndex--;

      [array[currentIndex], array[randomIndex]] = [
        array[randomIndex],
        array[currentIndex],
      ];
    }

    return array;
  };
  const generateSquares = () => {
    return shuffle(squareData).map((sq: any) => (
      <motion.div
        key={sq.id}
        layout
        transition={{ duration: 1.5, type: "spring" }}
        className="w-full h-full"
        style={{
          backgroundImage: `url(${sq.src})`,
          backgroundSize: "cover",
        }}
      ></motion.div>
    ));
  };

  const ShuffleGrid = () => {
    const timeoutRef = useRef<any>(null);
    const [squares, setSquares] = useState(generateSquares());

    useEffect(() => {
      shuffleSquares();

      return () => clearTimeout(timeoutRef.current);
    }, []);

    const shuffleSquares = () => {
      setSquares(generateSquares());

      timeoutRef.current = setTimeout(shuffleSquares, 3000);
    };

    return (
      <div className="grid grid-cols-4 grid-rows-3 h-[450px] gap-1">
        {squares.map((sq: any) => sq)}
      </div>
    );
  };

  return (
    <section className="bg-primary pt-5 lg:pt-[80px] z-10">
      <div className="container">
        <div className="grid lg:grid-cols-2 gap-[30px] items-center">
          <div>
            <h1 className="mb-3 text-white">
              Premium Expert Admission Guidance
            </h1>
            <div className="max-w-[556px]">
              <p className="text-white text-lg">
                As your education consultant and migration agent, we'll help you
                optimize your educational experience by finding the right
                universities and courses for you.
              </p>
            </div>
          </div>
          <ShuffleGrid />
        </div>
      </div>
    </section>
  );
};

export default ServiceLandingHero;
